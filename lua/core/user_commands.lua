local workspaces = require("plugins.projects.utils")

vim.api.nvim_create_user_command("GetMarks", function(opts)
    print(vim.inspect(workspaces.getMarks(opts.args)))
end, { nargs = 1 })

vim.api.nvim_create_user_command("AddMark", function(opts)
    workspaces.addMark(opts.fargs[1], opts.fargs[2])
end, { nargs = "+" })

vim.api.nvim_create_user_command("RmMark", function(opts)
    workspaces.rmMark(opts.fargs[1], opts.fargs[2])
end, { nargs = "+" })
